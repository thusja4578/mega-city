package model;

public class driver {
String name;
String address;
int contact;
String dob;
int idnum;
int licen;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}
public int getContact() {
	return contact;
}
public void setContact(int contact) {
	this.contact = contact;
}
public String getDob() {
	return dob;
}
public void setDob(String dob) {
	this.dob = dob;
}
public int getIdnum() {
	return idnum;
}
public void setIdnum(int idnum) {
	this.idnum = idnum;
}
public int getLicen() {
	return licen;
}
public void setLicen(int licen) {
	this.licen = licen;
}

}
